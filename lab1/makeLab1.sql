-- SQL script to create the tables necessary for lab 1 in EDA216.
-- MySQL version.
--
-- Creates the tables Students, Courses, TakenCourses and
-- populates them with (simulated) data.
--
-- We disable foreign key checks temporarily so we can delete the
-- tables in arbitrary order, and so insertion is faster.

set FOREIGN_KEY_CHECKS = 0;

-- Drop the tables if they already exist.

drop table if exists Students;
drop table if exists Courses;
drop table if exists TakenCourses;

-- Create the tables. The 'check' constraints are not effective in MySQL. 

create table Students (
    pNbr        char(11),
    firstName   varchar(20) not null,
    lastName    varchar(20) not null,
    primary key (pNbr)
);

create table Courses (
    courseCode  char(6),
    courseName  varchar(50) not null,
    credits     integer not null check (credits > 0),
    primary key (courseCode)
);

create table TakenCourses (
    pNbr        char(11),
    courseCode  char(6),
    grade       integer not null check (grade >= 3 and grade <= 5),
    primary key (pNbr, courseCode),
    foreign key (pNbr) references Students(pNbr),
    foreign key (courseCode) references Courses(courseCode)
);

-- We will do a lot of inserts, so we start a transaction to make it faster.

